-- MySQL-compatible dump for sample e-commerce dataset

CREATE DATABASE IF NOT EXISTS ecommerce_sample;
USE ecommerce_sample;

CREATE TABLE customers (
  customer_id INT PRIMARY KEY,
  name VARCHAR(200),
  email VARCHAR(200),
  location VARCHAR(200),
  signup_date DATE,
  age INT,
  gender VARCHAR(20)
);

CREATE TABLE products (
  product_id INT PRIMARY KEY,
  product_name VARCHAR(255),
  category VARCHAR(100),
  price DECIMAL(10,2),
  cost DECIMAL(10,2),
  stock INT
);

CREATE TABLE orders (
  order_id INT PRIMARY KEY,
  customer_id INT,
  order_date DATETIME,
  payment_type VARCHAR(50),
  status VARCHAR(50)
);

CREATE TABLE order_items (
  order_item_id INT PRIMARY KEY,
  order_id INT,
  product_id INT,
  quantity INT,
  unit_price DECIMAL(10,2)
);

